// Helper function to get posts by creator.
export function getPostsByCreator(creatorId: string): Post[] {
	return mockPosts.filter((post) => post.authorId === creatorId);
}

// Helper function to get entries by post.
export function getEntriesByPost(postId: string): Entry[] {
	return mockEntries.filter((entry) => entry.postId === postId);
}

